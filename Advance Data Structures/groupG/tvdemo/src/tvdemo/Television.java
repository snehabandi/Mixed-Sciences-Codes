package tvdemo;



public class Television {
	
	private int modelno,size,price;
	void setmodelno(int m)
	{
		modelno=m;
	}
	int getmodelno()
	{
		return modelno;
	}
	void setsize(int s)
	{
		size=s;
	}
	int getsize()
	{
		return size;
	}
	void setprice(int p)
	{
		price=p;
	}
	int getprice()
	{
		return price;
	}
}
